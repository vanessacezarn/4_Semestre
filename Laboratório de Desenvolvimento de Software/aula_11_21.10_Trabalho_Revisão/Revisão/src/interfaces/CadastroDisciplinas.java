    private void preencherProfessores(){
        ProfessoresDAO pDAO = new ProfessoresDAO();
        List<Professores> listaProf = pDAO.getProfessores();
        for (Professores p : listaProf){
            cmbProfessor.addItem(p);  
        }
    }

